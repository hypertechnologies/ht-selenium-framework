package basicjava;

public class variables {
    public static void main(String[] args) {

        System.out.println("Hello HyperTech");

        // String variable
        String str = "Hello world";
        System.out.println(str);

        str = str + " 2";
        System.out.println(str);

        // Int variable
        int myNum = 236589999;
        System.out.println(myNum);
        int negativeNum = -150;
        System.out.println(negativeNum);

        // float variable
        float myFloat = 123.09565f;
        System.out.println(myFloat);
        float negFloat = -123.09565f;
        System.out.println(negFloat);

        // Double variable
        double myDouble = 500.06;
        System.out.println(myDouble);
        double negDouble = -500.06;
        System.out.println(negDouble);

        // char variable
        char myLetter = 'E';
        System.out.println(myLetter);
        char anotherLetter = 'Z';
        System.out.println(anotherLetter);

        // boolean variable
        boolean myBool = true;
        System.out.println(myBool);
        boolean anotherBool = false;
        System.out.println(anotherBool);

        // Define a float with value of 106.98 and print it
        // Define a negative double with value  of 985.55 and print it.

        // byte variable - can only contain -128 to 127
        byte myByte = 104;
        System.out.println(myByte);
       // byte myByte2 = 128; //this is wrong
        byte myByte3 = -104;
        System.out.println(myByte3);

        // short variable max 32767, min -32768
        short myShort = 32767;
        System.out.println(myShort);
        // short myShort2 = 32768; // this is wrong

        // long variable
        long myLong = 7952452634735364566l;
        System.out.println(myLong);




    }


}
